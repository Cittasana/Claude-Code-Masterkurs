function badge(id: string): LeaderboardBadge {
  return LEADERBOARD_BADGES.find(b => b.id === id)!;
}

function daysAgo(days: number): string {
  const d = new Date();
  d.setDate(d.getDate() - days);
  return d.toISOString();
}

